CREATE TABLE Bateau(
   bateau_id serial PRIMARY KEY,
   bateau_nom VARCHAR (70) UNIQUE NOT NULL,
   bateau_type VARCHAR (50) CHECK ( bateau_type IN ('Multi50', 'IMOCA', 'Class40', 'ORMA', 'Classe 2', 'MOD70', 'Ultime'))
);


CREATE TABLE Edition(
   edition_num serial PRIMARY KEY,
   edition_date_depart DATE NOT NULL,
   edition_port_depart VARCHAR (70) NOT NULL,
   edition_port_arrivee  VARCHAR (70) NOT NULL
);


CREATE TABLE Skipper(
   skipper_id serial PRIMARY KEY,
   skipper_nom VARCHAR(70) NOT NULL,
   skipper_prenom VARCHAR (70) NOT NULL,
   skipper_nationalite  VARCHAR (70) NOT NULL
);

CREATE TABLE Participe(
   edition_num serial REFERENCES  Edition(edition_num) ,
   skipper_id serial REFERENCES Skipper(skipper_id),
   co_skipper_id serial REFERENCES Skipper(skipper_id),
   bateau_id serial REFERENCES Bateau(bateau_id),
   PRIMARY KEY(edition_num, skipper_id)
);

CREATE TABLE Arrive(
  edition_num serial REFERENCES Edition(edition_num),
  bateau_id   serial REFERENCES Bateau(bateau_id),
  date_arrivee TIMESTAMP,
  PRIMARY KEY(edition_num, bateau_id)
);


